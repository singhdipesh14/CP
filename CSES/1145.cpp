void func()
{
	int n;
	cin >> n;
	vector<int> nums(n);
	for (int &i : nums)
	{
		cin >> i;
	}
	int dp[n];
	for (int i = 0; i < n; i++)
	{
		dp[i] = 1;
	}
	int i = 0;
	int ans = 0;
	while (i < n)
	{
		int j = 0;
		while (j < i)
		{
			if (nums[i] > nums[j] && dp[i] <= dp[j])
			{
				dp[i] = 1 + dp[j];
				ans = max(ans, dp[i]);
			}
			j++;
		}
		i++;
	}
	cout << (ans == 0 ? 1 : ans) << '\n';
}

