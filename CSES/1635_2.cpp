const int mod = 1e9 + 7;

void func()
{
	int n, target;
	cin >> n >> target;
	vector<int> nums(n);
	vector<int> coins(target + 1, 0);
	for (int &i : nums)
	{
		cin >> i;
	}
	coins[0] = 1;
	for (int i = 1; i <= target; i++)
	{
		for (int j = 0; j < n; j++)
		{
			if (i - nums[j] >= 0)
			{
				coins[i] = coins[i] % mod + coins[i - nums[j]] % mod;
			}
		}
	}
	cout << coins[target] % mod << '\n';
}

